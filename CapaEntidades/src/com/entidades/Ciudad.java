package com.entidades;

public class Ciudad {
	
	private int idCiudad;
	private String nombreCiudad;
	private String direccionCiudad;
	private Boolean estadoCiudad;
	
	public int getIdCiudad() {
		return idCiudad;
	}
	public void setIdCiudad(int idCiudad) {
		this.idCiudad = idCiudad;
	}
	public String getNombreCiudad() {
		return nombreCiudad;
	}
	public void setNombreCiudad(String nombreCiudad) {
		this.nombreCiudad = nombreCiudad;
	}
	public String getDireccionCiudad() {
		return direccionCiudad;
	}
	public void setDireccionCiudad(String direccionCiudad) {
		this.direccionCiudad = direccionCiudad;
	}
	public Boolean getEstadoCiudad() {
		return estadoCiudad;
	}
	public void setEstadoCiudad(Boolean estadoCiudad) {
		this.estadoCiudad = estadoCiudad;
	}
	
	
}
