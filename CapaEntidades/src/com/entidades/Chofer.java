package com.entidades;

public class Chofer {
	
	private int idChofer;
	private String nombreChofer;
	private String apellidoChofer;
	private String telefonoChofer;
	private String dniChofer;
	private Boolean estadoChofer;
	
	public int getIdChofer() {
		return idChofer;
	}
	public void setIdChofer(int idChofer) {
		this.idChofer = idChofer;
	}
	public String getNombreChofer() {
		return nombreChofer;
	}
	public void setNombreChofer(String nombreChofer) {
		this.nombreChofer = nombreChofer;
	}
	public String getApellidoChofer() {
		return apellidoChofer;
	}
	public void setApellidoChofer(String apellidoChofer) {
		this.apellidoChofer = apellidoChofer;
	}
	public String getTelefonoChofer() {
		return telefonoChofer;
	}
	public void setTelefonoChofer(String telefonoChofer) {
		this.telefonoChofer = telefonoChofer;
	}
	public String getDniChofer() {
		return dniChofer;
	}
	public void setDniChofer(String dniChofer) {
		this.dniChofer = dniChofer;
	}
	public Boolean getEstadoChofer() {
		return estadoChofer;
	}
	public void setEstadoChofer(Boolean estadoChofer) {
		this.estadoChofer = estadoChofer;
	}

	
}
